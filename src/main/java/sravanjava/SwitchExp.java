package sravanjava;

public class SwitchExp
{
    public static void main(String[] args) {
        int k=12124;
        switch (k)
        {
            case 1:
                System.out.println("1st stmt");
                break;
            case 2:
                System.out.println("2nd switch");
                break;
            case 12124:
                System.out.println("case 3");
                break;
            default:
                System.out.println("def");

        }
    }
}
